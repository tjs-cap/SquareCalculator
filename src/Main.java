public class Main {
    public static void main(String[] args) {
        double width = 13.5;
        double height = 25.3;
        double perimeter;
        double area;

        System.out.println("Width = " + width);
        System.out.println("Height = " + height);

        perimeter = width * 2 + height * 2;
        System.out.println("Perimeter = " + perimeter);

        area = width * height;
        System.out.println("Area = " + area);


    }
}
